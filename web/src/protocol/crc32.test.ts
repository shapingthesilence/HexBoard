import { describe, expect, it } from "vitest";
import { crc32 } from "./crc32.ts";

describe("crc32", () => {
  it("matches the standard check vector used by the firmware polynomial", () => {
    expect(crc32(new TextEncoder().encode("123456789"))).toBe(0xcbf43926);
  });

  it("matches the draft 19 EDO object snippet", () => {
    const raw = Uint8Array.from([
      0x48, 0x42, 0x53, 0x31, 0x03, 0x01, 0x00, 0x00,
      0x01, 0x06, 0x00, 0x31, 0x39, 0x20, 0x45, 0x44, 0x4f,
      0x20, 0x01, 0x00, 0x01,
      0x21, 0x02, 0x00, 0x13, 0x00,
      0x22, 0x04, 0x00, 0x80, 0x4f, 0x12, 0x00
    ]);
    expect(crc32(raw)).toBe(0x6702fe2b);
  });
});

